def main():
    dataset = font_dataset_to_matrix(Font3)

    autoencoder = BasicAutoEncoder(
        dataset=dataset,
        hidden_encoder_layers_to_latent_space=[3, 2], 
        activation_func=ActivationFunction.LOGISTICS,
        learn_rate=0.05,
        min_error=0.01,
        max_epochs=1000
    )

    while autoencoder.has_next():
        autoencoder.next_epoch()
        print(f"Epoch {autoencoder.current_epoch}, error: {autoencoder.error:.5f}")

    # Visualización del primer carácter (índice 0)
    original = dataset[2].reshape(7, 5)
    reconstructed_flat = autoencoder.try_current_epoch(dataset[2])
    reconstructed = reconstructed_flat.reshape(7, 5)

    fig, axs = plt.subplots(1, 2, figsize=(6, 3))
    sns.heatmap(original, cbar=False, linewidths=0.2, linecolor='black', square=True, ax=axs[0])
    axs[0].set_title("Original")

    sns.heatmap(reconstructed, cbar=False, linewidths=0.2, linecolor='black', square=True, ax=axs[1])
    axs[1].set_title("Reconstruido")

    plt.tight_layout()
    plt.show()

if __name__ == "__main__":
    main()
